var side='down';

switch (side) {
    case 'up':
    alert('верхняя сторона');
    break;
    case 'right':
    alert('правая сторона');
    break;
    case 'down':
    alert('нижняя сторона');
    break;
    case 'left':
    alert('левая сторона');
    break;
    default:
    alert('неизвестная сторона');
}